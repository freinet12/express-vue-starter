'use strict';

const fs = require('fs');
const path = require('path');
const Sequelize = require('sequelize');
const basename = path.basename(__filename);
const env = process.env.NODE_ENV || 'development';
const config = require('../../config/database');
const db = {};
//set main database connection
const DefaultConenction = process.env.DEFAULT_DB_CONNECTION ? process.env.DEFAULT_DB_CONNECTION : 'mysql';
const connection = config.connections[DefaultConenction][env];

const sequelize = new Sequelize(connection.database, connection.username, connection.password, connection);
//map all the models to this connection
fs
  .readdirSync(__dirname)
  .filter(file => {
    return (file.indexOf('.') !== 0) && (file !== basename) && (file.slice(-3) === '.js');
  })
  .forEach(file => {
    const model = sequelize['import'](path.join(__dirname, file));
    db[model.name] = model;
  });

Object.keys(db).forEach(modelName => {
  if (db[modelName].associate) {
    db[modelName].associate(db);
  }
});

db.sequelize = sequelize;
db.Sequelize = Sequelize;

module.exports = db;
